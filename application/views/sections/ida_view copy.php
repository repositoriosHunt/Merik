<div class="row">
	<div class="col-sm-4 col-sm-offset-2 merik-txt-l animated">
		<p><strong>¿Qué es el IDA?</strong><br>IDA es la asociación internacional de puertas, (International Door Association, por sus siglas en inglés), con sede en West Milton, Ohio, EE.UU, que agrupa a los principales fabricantes, distribuidores, instaladores y negocios afines a las puertas y sistemas de acceso de Estados Unidos, Canadá, México, Reino Unido, Australia, algunos países de Europa y Asia.</p>
		<p><strong>Misión</strong><br>
			La misión principal de la Alianza Merik-IDA es proveer de programas y servicios a los distribuidores de puertas y sistemas de acceso con el propósito de mejorar su valor y calidad.
		</p>
		<p><strong>Historia</strong></p>
		<p>La asociación internacional de puertas (IDA) se creó en 1995 con la fusión de: </p>
		<ul class="list-unstyled">
			<li>La asociación de distribuidores de puertas y operadores.</li>
			<li>(DODA... Door & Operator Dealers Association, fundada en 1968).</li>
			<li>La asociación de puertas de cochera del lejano oeste.</li>
			<li>(FWGDA... Far Western Garage Door Association (fundada en 1964).</li>
		</ul>
		<p>La fusión se acordó entre las dos asociaciones el 14 de junio de 1994, dando inicio oficialmente como IDA a partir del 1 de enero de 1995.</p>
		<p>La asociación tiene su sede en West Milton, Ohio, EE.UU.</p>
	</div>
	<div class="col-sm-4 merik-txt-r animated">
		<p>IDA está trabajando en estrecha colaboración con sus organizaciones afiliadas dentro y fuera de los Estados Unidos, y actúa como una asociación independiente de los fabricantes de puertas y sistemas de operadores en los Estados Unidos, Canadá, México, Reino Unido y Australia. </p>
		<p>Organiza una exposición anual, la IDAExpo, que alterna sus sedes, siendo estas un año en Las Vegas, Nevada (lado oeste de los Estados Unidos) y al siguiente año en una sede del lado este.</p>
		<p><strong>¿Quiénes son sus miembros?</strong></p>
		<p>275 empresas industriales (fabricantes y proveedores) y 1,700 distribuidores que realizan la instalación y el mantenimiento de los productos.</p>
			<p><strong>Geografía de nuestra actividad</strong><br>
			Distribución de las empresas y de los miembros IDA ubicados en los siguientes países:<br>
		<ul>
			<li>76% - en EE.UU.</li>
			<li>12% - en México.</li>
			<li>6% - en el Canadá.</li>
			<li>6% - en otros países.</li>
		</ul>
			</p>
		<p><strong>Regiones IDA</strong><br>
			Con el fin de servir mejor a su significativa base de miembros, IDA está organizado en 12 regiones. Un director de IDA representa cada una de estas regiones.</p>
